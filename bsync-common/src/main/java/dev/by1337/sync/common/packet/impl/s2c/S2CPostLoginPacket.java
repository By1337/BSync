package dev.by1337.sync.common.packet.impl.s2c;

import dev.by1337.sync.common.packet.ByteBufCodecs;
import dev.by1337.sync.common.packet.Packet;
import dev.by1337.sync.common.packet.Packets;
import io.netty.buffer.ByteBuf;

import java.util.UUID;

public record S2CPostLoginPacket(UUID serverUid) implements Packet {

    public S2CPostLoginPacket(ByteBuf buf, int protocolVersion) {
        this(ByteBufCodecs.readUUID(buf));
    }

    @Override
    public void write(ByteBuf buf, int protocolVersion) {
        ByteBufCodecs.writeUUID(buf, serverUid);
    }

    @Override
    public int getId() {
        return Packets.C2S_POST_LOGIN_PACKET;
    }
}
